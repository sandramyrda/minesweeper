public class Location {
    int value = 0;
    boolean revealed = false;

    public Location(int value) {
        value = this.value;
    }

    public Location() {
    }

    public void setRevealed(boolean revealed) {
        this.revealed = revealed;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

}
